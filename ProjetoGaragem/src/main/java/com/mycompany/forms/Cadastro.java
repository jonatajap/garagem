    public static void initAluguel(Carro carro, Cliente cliente){
        
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("umbler_database");
        EntityManager em = emf.createEntityManager();

        em.getTransaction().begin();
        
        em.persist(cliente);
        em.persist(carro);
        
        Garagem garagem = new Garagem("Centro", 5);
        em.persist(garagem);
        
        Vaga vaga = garagem.procurarVagaDisponivel();
        em.persist(vaga);
        
        Aluguel aluguel = new Aluguel(cliente, vaga);
        
        aluguel.vagaOcupar();
       
        em.persist(aluguel);
       
        em.getTransaction().commit();

        em.close();
        emf.close();
    }
    
